package models;

public class Doctor {
	
	private int docId;
	private String docName;
	private String docSpl;
	
	public int getDocId() {
		return docId;
	}
	public void setDocId(int docId) {
		this.docId = docId;
	}
	public String getDocName() {
		return docName;
	}
	public void setDocName(String docName) {
		this.docName = docName;
	}
	public String getDocSpl() {
		return docSpl;
	}
	public void setDocSpl(String docSpl) {
		this.docSpl = docSpl;
	}	
}
